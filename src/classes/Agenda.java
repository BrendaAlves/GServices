package classes;

public class Agenda {
    private String buscaServico;
    private String buscaProduto;
    private String buscaMarcaProduto;
    private String buscaFuncionario;
    private String buscaBuscaData;

    public String getBuscaServico() {
        return buscaServico;
    }

    public void setBuscaServico(String buscaServico) {
        this.buscaServico = buscaServico;
    }

    public String getBuscaProduto() {
        return buscaProduto;
    }

    public void setBuscaProduto(String buscaProduto) {
        this.buscaProduto = buscaProduto;
    }

    public String getBuscaMarcaProduto() {
        return buscaMarcaProduto;
    }

    public void setBuscaMarcaProduto(String buscaMarcaProduto) {
        this.buscaMarcaProduto = buscaMarcaProduto;
    }

    public String getBuscaFuncionario() {
        return buscaFuncionario;
    }

    public void setBuscaFuncionario(String buscaFuncionario) {
        this.buscaFuncionario = buscaFuncionario;
    }

    public String getBuscaBuscaData() {
        return buscaBuscaData;
    }

    public void setBuscaBuscaData(String buscaBuscaData) {
        this.buscaBuscaData = buscaBuscaData;
    }
}
